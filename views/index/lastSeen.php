    function lastSeen__list(index) {
        let lastSeenTag = $('.lastSeen__list-images');
        let lastSeenItems = lastSeenTag.find('ul');
        let lastSeenSlide = index + 1;
        let lastSeenNav = $('.lastSeen__list-nav ul li');
        lastSeenItems.hide();
        lastSeenNav.removeClass('active');
        lastSeenItems.eq(lastSeenSlide - 1).show();
        lastSeenNav.eq(lastSeenSlide - 1).addClass('active');
    }

    lastSeen__list(1);

    function goToLastSeenIndex(tag) {
        let index = $(tag).index();
        lastSeen__list(index);
    }

